#41. Elabora um algoritmo onde o usuário informa a base e a altura de um retângulo e ao final imprime a área.

print()
print("** Calcule a base e a altura de um retângulo e mostre o resultado: Área **")
print()
base = int(input("Digite a base do retângulo: "))
altura = int(input("Digite a altura do retângulo: "))

area = (base * altura)*2
print()
print("A área do retângulo é {}.".format(area))
