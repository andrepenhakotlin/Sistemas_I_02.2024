#Questão 5. Faça um algoritmo que leia um número e mostra se o número é maior que 20,
#menor que 20 ou igual a 20.

#Autor: André L. B. Penha
#Data: 25.10.2024

numero = float(input("Digite um número: "))

if numero > 20:
    print("O número é maior que 20.")
elif numero < 20:
    print("O número é menor que 20.")
else:
    print("O número é igual a 20.")
